    public String[] toBanner(String input) {
        if (input == null)
            return new String[0];

        input = input.toUpperCase();

        String[] rows = new String[] {"", "", "", "", "", "", ""};
        int idx;
        char letter;

        for (int i = 0; i < input.length(); i++) {
            letter = input.charAt(i);

            if (letter >= 'A' && letter <= 'Z') {
                idx = letter - 'A';
                for (int j = 0; j < 7; j++)
                    rows[j] = rows[j] + ASCIIArt[idx][j] + " ";
            } 
            else if (letter == ' ') {
                for (int j = 0; j < 7; j++)
                    rows[j] += "   ";
            }
        }
        return rows;
    }
